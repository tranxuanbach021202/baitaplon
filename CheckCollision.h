#ifndef CHECKCOLLISION_H_
#define CHECKCOLLISION_H_

#include "Commonfunction.h"
#include "Planes.h"
#include "Planes_Enemy.h"
#include "Load_Texture.h"

static bool Check_Collision (SDL_Rect a , SDL_Rect b)
{
     //The sides of the rectangles
    int leftA, leftB;
    int rightA, rightB;
    int topA, topB;
    int bottomA, bottomB;

    //Calculate the sides of rect A
    leftA = a.x;
    rightA = a.x + a.w;
    topA = a.y;
    bottomA = a.y + a.h;

    //Calculate the sides of rect B
    leftB = b.x;
    rightB = b.x + b.w;
    topB = b.y;
    bottomB = b.y + b.h;

    //If any of the sides from A are outside of B
    if( bottomA <= topB )
    {
        return false;
    }

    if( topA >= bottomB )
    {
        return false;
    }

    if( rightA <= leftB )
    {
        return false;
    }

    if( leftA >= rightB )
    {
        return false;
    }

    //If none of the sides from A are outside B
    return true;
}
#endif // CHECKCOLLISION_H_

